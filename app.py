def soma(a: float, b: float) -> float:
    """
    Soma dois números.

    Args:
        a (float): Primeiro número.
        b (float): Segundo número.

    Returns:
        float: A soma de a e b.
    """
    return a + b

def subtrai(a: float, b: float) -> float:
    """
    Subtrai dois números.

    Args:
        a (float): Primeiro número.
        b (float): Segundo número.

    Returns:
        float: A subtração de a por b.
    """
    return a - b
